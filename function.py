def happy_birthday(name, age):
    print(f"happy birthday to {name}")
    print(f"you are {age} years old")
    print("happy birthday to you")
    print()

happy_birthday("Alice", 20)
happy_birthday("Bob", 25)
happy_birthday("Charlie", 30)